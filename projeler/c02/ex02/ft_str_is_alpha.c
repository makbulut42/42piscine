#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (('A' <= str[i] && str[i] <= 'Z') \
		|| ('a' <= str[i] && str[i] <= 'z'))
	{
		i++;
	}
	if (str[i] == '\0')
		return (1);
	else
		return (0);
}
